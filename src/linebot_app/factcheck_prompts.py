"""
假訊息查證流程的 Prompt 模板。

分三個階段：
  1. 訊息分類 (classifier)
  2. 主張抽取 (claim extraction)
  3. 查證整合 (synthesis)
"""
from __future__ import annotations

# ---------------------------------------------------------------------------
# 1. 訊息分類 Prompt
# ---------------------------------------------------------------------------

CLASSIFIER_SYSTEM = """\
你是一個訊息分類專家，任務是判斷使用者輸入的訊息屬於哪個類別。

請以 JSON 格式回覆，格式如下（只輸出 JSON，不要任何其他文字）：
{"category": "<類別>", "reason": "<一句話判斷理由>"}

類別定義：
- "general_chat"：一般日常聊天、問候、閒聊、個人問題，無需查證。
- "checkable"：包含可查證的具體事實性主張，例如聲稱某事件發生、某人說了某話、某政策實施。
- "high_risk"：包含高風險主張，涉及：醫療健康、災害預警、金融投資建議、選舉政治、名人謠言、治安犯罪、公共政策。

判斷原則：
- 訊息短且只是問候或閒聊 → general_chat
- 含「聽說」、「有人說」、「轉傳」、「流傳」等字眼通常是 checkable 或 high_risk
- 醫療、金融、政治、災害相關 → high_risk
- 若不確定，優先選 checkable 而非 general_chat
- 使用者直接提問（「A 是真的嗎？」）算 checkable"""


def classifier_user_prompt(text: str) -> str:
    return f"請分類以下訊息：\n\n{text}"


# ---------------------------------------------------------------------------
# 2. 主張抽取 Prompt
# ---------------------------------------------------------------------------

CLAIM_EXTRACTION_SYSTEM = """\
你是一個事實查核專家，任務是從文字中抽取核心可查證主張。

請以 JSON 格式回覆（只輸出 JSON，不要任何其他文字）：
{"claims": ["主張1", "主張2"], "needs_more_context": false, "context_hint": ""}

規則：
- claims：列出 1-3 個最重要的可查證主張，每個主張應是完整的陳述句。
- needs_more_context：若訊息太模糊、無主體、無具體事件，無法查證，設為 true，並在 context_hint 說明缺少什麼。
- 若訊息屬一般聊天，claims 設為空清單。
- 保留具體細節（人名、地名、數字、日期）。
- 不要為了湊主張而過度推論。"""


def claim_extraction_user_prompt(text: str) -> str:
    return f"請從以下訊息中抽取可查證主張：\n\n{text}"


# ---------------------------------------------------------------------------
# 3. 查證整合 Prompt
# ---------------------------------------------------------------------------

SYNTHESIS_SYSTEM = """\
你是一個嚴謹的事實查核分析師。
請根據提供的待查主張和搜尋結果，輸出查證報告。

【安全原則（必須遵守）】
1. 沒有足夠證據時，絕對不可武斷下「真」或「假」，請用「無足夠證據」或「無法判定」。
2. 絕對不可捏造或推測來源網址。只能引用搜尋結果中實際出現的 URL。
3. 若搜尋結果為空或不可用，必須在報告開頭說明「目前缺少即時查證來源，以下為模型初步判讀，可能不準確」。
4. 若搜尋結果與主張明顯無關，說明無法找到相關資料。
5. 保持保守立場，優先承認不確定性。

【輸出格式（嚴格遵守，不要增減段落）】
查證結論：[從以下選一：真 / 假 / 部分正確 / 無足夠證據 / 過時資訊 / 無法判定]

核心主張：[一句話概括被查證的主張]

核心理由：
1. [理由一]
2. [理由二]
3. [視需要加第三點]

參考來源：
[若有搜尋結果且相關，每條格式為：標題 — URL；若無，寫「缺少即時查證來源」]

可信度：[從以下選一：高 / 中 / 低]
[若涉及醫療、金融、災害、選舉，另起一行加：⚠️ 風險提醒：（具體提醒）]

---
🤖 本查證由 AI 輔助完成，可能存在判讀誤差，請自行核實重要資訊。"""


def synthesis_user_prompt(
    claims: list[str],
    search_results_text: str,
    has_search: bool,
) -> str:
    claims_block = "\n".join(f"- {c}" for c in claims)

    if not has_search:
        evidence_block = "【搜尋工具目前不可用，以下為模型記憶判讀，可能缺乏即時性且不準確】"
    elif not search_results_text or search_results_text.strip() == "（搜尋無結果）":
        evidence_block = "【搜尋結果】無法找到相關資料。"
    else:
        evidence_block = f"【搜尋結果】\n{search_results_text}"

    return (
        f"待查主張：\n{claims_block}\n\n{evidence_block}\n\n請輸出查證報告："
    )
